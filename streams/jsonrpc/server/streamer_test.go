// mockStateEngine implements StateEngine
type mockStateEngine struct {
	mu           sync.Mutex
	sub          *engine.Subscription
	unsubscribed bool
}

func newMockStateEngine(c <-chan *engine.State, done <-chan struct{}) *mockStateEngine {
	return &mockStateEngine{
		sub: engine.NewSubscription(c, done),
	}
}

func (m *mockStateEngine) Subscribe() (*engine.Subscription, func()) {
	m.mu.Lock()
	defer m.mu.Unlock()
	m.unsubscribed = false
	unsubscribe := func() {
		m.mu.Lock()
		defer m.mu.Unlock()
		m.unsubscribed = true
	}
	return m.sub, unsubscribe
}

func (m *mockStateEngine) isUnsubscribed() bool {
	m.mu.Lock()
	defer m.mu.Unlock()
	return m.unsubscribed
}

// mockStateDiffer implements StateDiffer
type mockStateDiffer struct{}

// Diff correctly returns (diff, error) to match the interface
func (m *mockStateDiffer) Diff(prev, new *engine.State) (*differ.StateDiff, error) {
	diff := &differ.StateDiff{}
	if prev != nil {
		diff.FromBlock = prev.Block.Number.Uint64()
	}
	if new != nil {
		diff.ToBlock = new.Block
	}
	return diff, nil
}

// MockStateStreamer simulates the RPC server behavior for Client tests
type MockStateStreamer struct {
	events chan *SubscriptionEvent
	t      *testing.T
}

// Subscribe matches the standard "defi_subscribe" call generated by the client
func (streamer *MockStateStreamer) StateStream(ctx context.Context) (*rpc.Subscription, error) {
	notifier, supported := rpc.NotifierFromContext(ctx)
	if !supported {
		return nil, rpc.ErrNotificationsUnsupported
	}

	rpcSub := notifier.CreateSubscription()
	go func() {
		for event := range streamer.events {
			select {
			case <-rpcSub.Err():
				return
			default:
				if err := notifier.Notify(rpcSub.ID, event); err != nil {
					streamer.t.Logf("Error notifying subscriber: %v", err)
					return
				}
			}
		}
	}()
	return rpcSub, nil
}

func SetupMockStateStreamer(ctx context.Context, t *testing.T, port int, events []*SubscriptionEvent) (<-chan error, error) {
	eventChan := make(chan *SubscriptionEvent, len(events))
	for _, e := range events {
		eventChan <- e
	}
	close(eventChan)

	streamer := &MockStateStreamer{events: eventChan, t: t}
	server := rpc.NewServer()
	if err := server.RegisterName("defi", streamer); err != nil {
		return nil, fmt.Errorf("failed to register API: %v", err)
	}

	wsHandler := server.WebsocketHandler([]string{"*"})
	httpServer := &http.Server{Addr: fmt.Sprintf(":%d", port), Handler: wsHandler}

	errChan := make(chan error, 1)
	go func() {
		if err := httpServer.ListenAndServe(); err != nil && err != http.ErrServerClosed {
			errChan <- err
		}
	}()
	go func() {
		<-ctx.Done()
		server.Stop()
		_ = httpServer.Shutdown(context.Background())
	}()

	return errChan, nil
}

