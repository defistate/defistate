// TestStateStreamerServer tests the Server Logic (StateStreamer)
func TestStateStreamerServer(t *testing.T) {
	t.Run("should send full state, then diffs, then full state again", func(t *testing.T) {
		stateCh := make(chan *engine.State, 5)
		mockStateEngine := newMockStateEngine(stateCh, make(chan struct{}))
		differ := &mockStateDiffer{}

		// Use a no-op logger for tests
		logger := slog.New(slog.NewTextHandler(io.Discard, nil))

		streamer, err := NewStateStreamer(&Config{
			Engine:            mockStateEngine,
			Differ:            differ,
			Logger:            logger,
			FullStateInterval: 3,
		})
		require.NoError(t, err)

		ctx, cancel := context.WithCancel(context.Background())
		defer cancel()
		_, err = StartStreamer(ctx, streamer, 18889, []string{""})
		require.NoError(t, err)

		client, err := rpc.DialContext(ctx, "ws://localhost:18889")
		require.NoError(t, err)
		defer client.Close()

		ch := make(chan *SubscriptionEvent)
		sub, err := client.Subscribe(ctx, RpcNamespace, ch, StateStreamSubscriptionMethod)
		require.NoError(t, err)

		readEvent := func() *SubscriptionEvent {
			select {
			case event := <-ch:
				return event
			case <-time.After(5 * time.Second): // Increased Timeout
				t.Fatal("Timeout waiting for event")
				return nil
			}
		}

		// 1. Full State (First)
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(100)}}
		require.Equal(t, EventTypeFull, readEvent().Type)

		// 2. Diff
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(101)}}
		require.Equal(t, EventTypeDiff, readEvent().Type)

		// 3. Diff
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(102)}}
		require.Equal(t, EventTypeDiff, readEvent().Type)

		// 4. Full State (Interval)
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(103)}}
		require.Equal(t, EventTypeFull, readEvent().Type)

		sub.Unsubscribe()
		require.Eventually(t, mockStateEngine.isUnsubscribed, 2*time.Second, 50*time.Millisecond)
	})

	t.Run("should force full state after an error to self-heal", func(t *testing.T) {
		stateCh := make(chan *engine.State, 5)
		mockStateEngine := newMockStateEngine(stateCh, make(chan struct{}))
		differ := &mockStateDiffer{}
		logger := slog.New(slog.NewTextHandler(io.Discard, nil))

		streamer, err := NewStateStreamer(&Config{
			Engine:            mockStateEngine,
			Differ:            differ,
			Logger:            logger,
			FullStateInterval: 4,
		})
		require.NoError(t, err)

		ctx, cancel := context.WithCancel(context.Background())
		defer cancel()
		_, err = StartStreamer(ctx, streamer, 18891, []string{""})
		require.NoError(t, err)

		client, err := rpc.DialContext(ctx, "ws://localhost:18891")
		require.NoError(t, err)
		defer client.Close()

		ch := make(chan *SubscriptionEvent)
		sub, err := client.Subscribe(ctx, RpcNamespace, ch, StateStreamSubscriptionMethod)
		require.NoError(t, err)

		readEvent := func() *SubscriptionEvent {
			select {
			case event := <-ch:
				return event
			case <-sub.Err():
				return nil
			case <-time.After(5 * time.Second): // Increased Timeout
				t.Fatal("Timeout waiting for event")
				return nil
			}
		}

		// 1. Full
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(100)}}
		require.Equal(t, EventTypeFull, readEvent().Type)

		// 2. Error -> Full
		stateWithError := &engine.State{
			Block: engine.BlockSummary{Number: big.NewInt(101)},
			Protocols: map[engine.ProtocolID]engine.ProtocolState{
				"uniswap_v2": {Error: "failed"},
			},
		}
		stateCh <- stateWithError
		require.Equal(t, EventTypeFull, readEvent().Type)

		// 3. Next Healthy -> Full (Self-Heal)
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(102)}}
		require.Equal(t, EventTypeFull, readEvent().Type)

		// 4. Next Healthy -> Diff (Back to normal)
		stateCh <- &engine.State{Block: engine.BlockSummary{Number: big.NewInt(103)}}
		require.Equal(t, EventTypeDiff, readEvent().Type)
	})
}
